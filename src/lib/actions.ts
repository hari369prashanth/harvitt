export const createParent = async (data: ParentSchema) => {
  try {
    const user = await clerkClient.users.createUser({
      username: data.username,
      firstName: data.name,
      lastName: data.surname,
      publicMetadata: { role: "parent" },
    });

    const parent = await prisma.parent.create({
      data: {
        id: user.id,
        username: data.username,
        name: data.name,
        surname: data.surname,
        email: data.email || null, // Set to null if undefined
        phone: data.phone, 
        address: data.address,
        students: {
          connect: data.studentIds?.map((studentId) => ({ id: studentId })) || [],
        },
      },
    });

    return { success: true, error: false, parent };
  } catch (err) {
    console.error(err);
    return { success: false, error: true };
  }
};


export const updateParent = async (data: ParentSchema) => {
  if (!data.id) return { success: false, error: true };

  try {
    await clerkClient.users.updateUser(data.id, {
      username: data.username,
      firstName: data.name,
      lastName: data.surname,
    });

    const updatedParent = await prisma.parent.update({
      where: { id: data.id },
      data: {
        username: data.username,
        name: data.name,
        surname: data.surname,
        email: data.email || null,
        phone: data.phone,
        address: data.address,
        students: {
          set: [], // Unlink existing students
          connect: data.studentIds?.map((studentId) => ({ id: studentId })) || [],
        },
      },
    });

    return { success: true, error: false, updatedParent };
  } catch (err) {
    console.error(err);
    return { success: false, error: true };
  }
};

